public class Exemplo01 {
  public static void main(String[] args) {
    // System.out.println("Olá, Turma!");
    System.out.println("O dobro de 10.7 é " + dobro(10.7));
  }

  public static int dobro(int num) {
    return 2 * num;
  }

  public static double dobro(double num) {
    return 2 * num;
  }
}